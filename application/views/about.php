    <h2>About us</h2>
    <p>
        MyLoLChampions is founded by Tigran Gh. aka GhMaster - a professional web developer, and a huge League of Legends fan, in January 2014.
    </p>
    <blockquote>
        <p>
            After playing for 2 years and unlocking a lot of champions, I realized that it's pretty hard to decide on what champion to pick in particular matches. My own experience and a lot of comments from LoL community members about the current issue forced me to start thinking about creating such a tool that will let combining champions in corresponding groups and make the process of picking much easier.
            The main idea of this tool is to help players create their own champion groups like <b>My junglers</b>, <b>My top laners</b>, <b>Fun to play</b>, etc. Also the tool allows to search for champions based on their name, primary role and the custom group they belong to.
            You can even use this tool to get some random champions based on some criteria like 3 random champions from some custom group.
        </p>
        <footer class="pull-right">
            GhMaster, Founder
